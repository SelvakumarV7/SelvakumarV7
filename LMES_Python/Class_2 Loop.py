# 'for' is a Keyword/reserve word
# 'i' is just a variable
# 'in' is a Membership operator
# 'range' is a function

for i in range(50, 61):      #(starting range, ending range)
    print(i)
for i in range(35, 46):
    print(i)

for i in range(20):
    if i%2==1:
        print(i)

for i in range(1, 20, 3):           ##(starting range, ending range, step range)
    print(i)

for i in range(2, 21, 2):
    print(i)

for i in range(75, 101, 5):
    if i%5==0:
        print(i)

for i in range(49, 101, 7):
    print(i)